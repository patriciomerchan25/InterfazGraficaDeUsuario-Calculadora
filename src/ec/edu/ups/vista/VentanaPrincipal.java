    private void btnIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIgualActionPerformed

        String cad=txtIngreso.getText();

        for(int i=0;i<cad.length();i++){
            char ce=cad.charAt(i);
            if(ce=='+'){

                String n1=cad.substring(0,i);
                String n2=cad.substring(i+1,cad.length());
                double res=Double.parseDouble(n1)+Double.parseDouble(n2);
                txtIngreso.setText(String.valueOf(res));
            }else if(ce=='-'){
                String n1=cad.substring(0,i);
                String n2=cad.substring(i+1,cad.length());
                double res=Double.parseDouble(n1)-Double.parseDouble(n2);
                txtIngreso.setText(String.valueOf(res));

            }else if(ce=='*'){

                String n1=cad.substring(0,i);
                String n2=cad.substring(i+1,cad.length());
                double res=Double.parseDouble(n1)*Double.parseDouble(n2);
                txtIngreso.setText(String.valueOf(res));
            }else if (ce=='/'){
                String n1=cad.substring(0,i);
                String n2=cad.substring(i+1,cad.length());
                double res=Double.parseDouble(n1)/Double.parseDouble(n2);
                txtIngreso.setText(String.valueOf(res));
            }
        }

    }//GEN-LAST:event_btnIgualActionPerformed

